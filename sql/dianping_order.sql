DROP TABLE IF EXISTS `dp_order`;
CREATE TABLE `dp_order` (
  `id` BIGINT NOT NULL AUTO_INCREMENT,
  `order_no` VARCHAR(32) NOT NULL COMMENT '订单号',
  `user_id` BIGINT NOT NULL COMMENT '下单用户ID',
  `shop_id` BIGINT NOT NULL COMMENT '店铺ID',
  `coupon_id` BIGINT DEFAULT NULL COMMENT '优惠券ID',
  `amount` INT NOT NULL COMMENT '订单原价金额(分)',
  `pay_amount` INT DEFAULT 0 COMMENT '实付金额(分)',
  `status` INT NOT NULL DEFAULT 0 COMMENT '状态:0-待支付,1-已支付,2-已核销,3-已退款,4-已取消',
  `remark` VARCHAR(500) DEFAULT NULL COMMENT '备注',
  `paid_at` DATETIME DEFAULT NULL COMMENT '支付时间',
  `verified_at` DATETIME DEFAULT NULL COMMENT '核销时间',
  `cancelled_at` DATETIME DEFAULT NULL COMMENT '取消时间',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_order_no` (`order_no`),
  KEY `idx_order_user` (`user_id`),
  KEY `idx_order_shop` (`shop_id`),
  KEY `idx_order_status` (`status`),
  KEY `idx_order_created_at` (`created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='订单表';

